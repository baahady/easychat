new Vue({
	el:'#timeline',
	data:{
		post:'',
		posts:[],
		limit:5,
	},
	methods:{ 
		postStatus(e){
			e.preventDefault();
			$.ajax({
				url:'/posts',
				type:'post',
				dataType:'json',
				data:{
					'body':this.post
				},
				success:function(data){
					this.post=""
					this.posts.unshift(data);
				}.bind(this),
				error:function(){
					//
				}
			});
		},
		getPosts(){
			$.ajax({
				url:'/posts',
				dataType:'json',
				type:'get',
				data:{
					limit: this.limit
				},
				success:function(data){
					this.posts=data.posts
				}.bind(this),
			});
		}
	},
	mounted(){
		this.getPosts();

		setInterval(function(){
			this.getPosts();
		}.bind(this),1000);
	}
});